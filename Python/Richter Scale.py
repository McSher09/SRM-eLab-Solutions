mag=float(input())
if mag>=10.0:
  print("Meteoric")
elif mag>=8.0:
  print("Great")
elif mag>=7.0:
  print("Major")
elif mag>=6.0:
  print("Strong")
elif mag>=5.0:
  print("Moderate")
elif mag>=4.0:
  print("Light")
elif mag>=4.0:
  print("Minor")
elif mag>=3.0:
  print("Very minor")
else:
  print("Micro")
